// ── Derived Exports ───────────────────────────────────────────────────────

export const ALL_CONNECTORS = Object.values(CONNECTOR_REGISTRY);

export const DB_PROVIDERS = ALL_CONNECTORS
  .filter(c => c.usesConnectionProfile !== false)
  .map(c => c.value);

export const getConnectorMeta = (key) =>
  CONNECTOR_REGISTRY[key] ?? { value: key, label: key, icon: Database, color: '#888', category: 'enterprise', tags: [], capabilities: { source: true, destination: false } };

export const getPopularConnectors = () =>
  ALL_CONNECTORS.filter(c => c.popularityRank != null)
    .sort((a, b) => a.popularityRank - b.popularityRank);

export const getConnectorsByCategory = (categoryId, mode = 'all') => {
  let list = ALL_CONNECTORS;
  if (mode === 'source') list = list.filter(c => c.capabilities.source);
  if (mode === 'destination') list = list.filter(c => c.capabilities.destination);
  if (categoryId === 'all') return list;
  if (categoryId === 'popular') return list.filter(c => c.popularityRank != null).sort((a, b) => a.popularityRank - b.popularityRank);
  return list.filter(c => c.category === categoryId);
};
